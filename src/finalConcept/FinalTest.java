package finalConcept;

public class FinalTest {

	public static void main(String[] args) {
		
		/*
		 * with final key word you define constants, which mean value cannot be changed
		 */
		final int a = 10;
		
		
		System.out.println(a);
		
		
		/*
		 * final keyword is used to avoid inheritance
		 * create the class with final key word. by doing so no class can inherit the class
		 * 
		 * public final class ParentClass{}
		 */
		
		/*
		 * final keyword is used to avoid method overiding
		 * declare the method with final key word in the parent class
		 * 
		 * public final void methodname(){}
		 * 
		 */

	}

}
